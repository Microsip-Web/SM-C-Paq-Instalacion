// note Get selected value from select element
/**
 * It gets the value of the selected option in the dropdown menu.
 * @returns The value of the selected option.
 */
const getSelectedValue = () => {
	const selectedValue = document.getElementById('menuSelector').value;
	return selectedValue;
};

// note fires getSelectedValue function and display content
/**
 * When the user selects a value from the dropdown, display the corresponding pack.
 */
const displaySelectedValue = () => {
	const selectedValue = getSelectedValue();
	switch (selectedValue) {
		case '2022':
			displayPack2022();
			break;
		case '2021':
			displayPack2021();
			break;
		case '2020':
			displayPack2020();
			break;
		case '2019':
			displayPack2019();
			break;
		case 'Legado':
			displayPackLegado();
		default:
			break;
	}
};

